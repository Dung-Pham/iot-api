USE `iot-db`;

CREATE TABLE Users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    userName VARCHAR(255),
    email VARCHAR(255),
    password VARCHAR(255),
    deviceId VARCHAR(255)
);

CREATE TABLE Airs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    ppm FLOAT,
    deviceId VARCHAR(255),
    time TIME,
    date DATE
);

CREATE TABLE Lights (
    id INT AUTO_INCREMENT PRIMARY KEY,
    lux FLOAT,
    deviceId VARCHAR(255),
    time TIME,
    date DATE
);

CREATE TABLE Rains (
    id INT AUTO_INCREMENT PRIMARY KEY,
    status INT,
    deviceId VARCHAR(255),
    time TIME,
    date DATE
);

